; test for the MOD instruction
	
	; 0x12345678 mod 2 = 0
	LOAD32	R0,	@0x12345678
	LOAD32	R1,	@0x02
	XOR	R2,	R2,	R2
	MOD	R0,	R0,	R1
	CMP	R0,	R2
	FAILNZ	0x01
	
	; 0xFFFFFFFF mod 2 = 1
	LOAD32	R0,	@0xFFFFFFFF
	LOAD32	R1,	@0x02
	XOR	R2,	R2,	R2
	LOADI	R2LL,	0x01
	MOD	R0,	R0,	R1
	CMP	R0,	R2
	FAILNZ	0x02
	
	; 0xFFFFFFFE mod 0xFFFFFFFF = 0xFFFFFFFE
	LOAD32	R0,	@0xFFFFFFFE
	LOAD32	R1,	@0xFFFFFFFF
	LOAD32	R2,	@0xFFFFFFFE
	MOD	R0,	R0,	R1
	CMP	R0,	R2
	FAILNZ	0x03
	
	; 0x02 mod 0x05 = 0x02
	LOAD32	R0,	@0x02
	LOAD32	R1,	@0x05
	XOR	R2,	R2,	R2
	LOADI	R2LL,	0x02
	MOD	R0,	R0,	R1
	CMP	R0,	R2
	FAILNZ	0x04
	
